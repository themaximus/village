/// <summary>
/// Интерфейс для любого объекта, который может быть сохранен.
/// Любой скрипт, реализующий этот интерфейс, должен предоставить
/// методы для сохранения и загрузки своего состояния.
/// </summary>
public interface ISaveable
{
    /// <summary>
    /// "Фотографирует" текущее состояние объекта и возвращает его в виде объекта.
    /// Этот объект затем будет сериализован в JSON.
    /// </summary>
    /// <returns>Объект, содержащий данные для сохранения.</returns>
    object CaptureState();

    /// <summary>
    /// Восстанавливает состояние объекта из предоставленных данных.
    /// </summary>
    /// <param name="state">Объект, десериализованный из JSON, содержащий сохраненные данные.</param>
    void RestoreState(object state);
}
